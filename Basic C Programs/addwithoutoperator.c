//                                  //PROGRAM TO ADD NUMBERS WITHOUT '+' OPERATOR
#include<stdio.h>

void main(){
    int x,y;
    printf("ENTER THE TWO NUMBERS : ");
    scanf("%d %d",&x,&y);
    while (y!=0){
        x++;
        y--;
    }
    printf("THE SUM OF THE TWO NUMBERS WITHOUT USING + OPERATOR IS : %d", x);
}

